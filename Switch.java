public class Switch {
    public static void main(String a[]){
        int n = 3;
        switch(n){
        case 1: 
        System.out.println("monday");
        break;
        case 2:
        System.out.println("tuesday");
        break;
        case 3:
        System.out.println("wednesday");
        break;
        default:
        System.out.println("incorrect");
    }
    }
}
